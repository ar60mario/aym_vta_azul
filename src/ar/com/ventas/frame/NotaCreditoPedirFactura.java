    private void notaCreditoWebBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_notaCreditoWebBtnActionPerformed
        Double saldoCliente;
        if (cliente.getSaldo() != null) {
            saldoCliente = cliente.getSaldo();
        } else {
            saldoCliente = 0.0;
        }
        String s0 = df.format(saldoCliente);
        Double s1 = Double.valueOf(s0.replace(",", "."));
        if (s1 < 0.01) {
            int s3 = JOptionPane.showConfirmDialog(this, "SALDO: " + s0 + "\nVerifique el saldo antes de CONFIRMAR!!", "Atención", JOptionPane.YES_NO_OPTION);
            if (s3 == 1) {
                return;
            }
        }
        NotaCreditoWebDeFacturaFrame ncf = new NotaCreditoWebDeFacturaFrame(cliente, factura);
        ncf.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_notaCreditoWebBtnActionPerformed

